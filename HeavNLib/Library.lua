function Library:MakeDraggable(Instance, Cutoff)
    Instance.Active = true;

    local dragStart, startPos, dragging = nil, nil, false

    Instance.InputBegan:Connect(function(Input)
        if Input.UserInputType == Enum.UserInputType.MouseButton1 then
            local mouseY = Mouse.Y - Instance.AbsolutePosition.Y
            
            if mouseY > (Cutoff or 40) then
                return;
            end;

            dragging = true
            dragStart = Vector2.new(Mouse.X, Mouse.Y)
            startPos = Instance.Position

            local dragConnection
            dragConnection = RenderStepped:Connect(function()
                if not dragging or not InputService:IsMouseButtonPressed(Enum.UserInputType.MouseButton1) then
                    dragging = false
                    dragConnection:Disconnect()
                    return
                end

                local delta = Vector2.new(Mouse.X - dragStart.X, Mouse.Y - dragStart.Y)
                Instance.Position = UDim2.new(
                    startPos.X.Scale,
                    startPos.X.Offset + delta.X,
                    startPos.Y.Scale,
                    startPos.Y.Offset + delta.Y
                );
            end)
        end;
    end)
end;

