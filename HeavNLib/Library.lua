function Library:InputMatchesMenuKey(input)
    local key = self:GetMenuKeyName()
    if key == 'MB1' then
        return input.UserInputType == Enum.UserInputType.MouseButton1
    end
    if key == 'MB2' then
        return input.UserInputType == Enum.UserInputType.MouseButton2
    end
