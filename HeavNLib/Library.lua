        function Dropdown:BuildDropdownList()
            local Values = Dropdown.Values;
            local Buttons = {};

            for _, Element in next, Scrolling:GetChildren() do
                if not Element:IsA('UIListLayout') then
                    Element:Destroy();
                end;
            end;

            local Count = 0;
            local LAZY_LOAD_THRESHOLD = 15
            local INITIAL_LOAD_COUNT = 20
            local lazyLoadEnabled = #Values > LAZY_LOAD_THRESHOLD
            local renderedCount = 0
            local allValues = Values
            local loadingMore = false

            -- Lazy load function to add items one by one
            local function loadMoreItems(batchSize)
                if loadingMore then return end
                loadingMore = true
                
                batchSize = batchSize or 1
                local loaded = 0
                
                while loaded < batchSize and renderedCount < #allValues do
                    renderedCount = renderedCount + 1
                    local Value = allValues[renderedCount]
                    if not Value then break end
                    
                    local Table = {}
                    Count = Count + 1
                    loaded = loaded + 1

                    local Button = Library:Create('Frame', {
                        BackgroundColor3 = Library.MainColor;
                        BorderColor3 = Library.OutlineColor;
                        BorderMode = Enum.BorderMode.Middle;
                        Size = UDim2.new(1, -1, 0, 20);
                        ZIndex = 23;
                        Active = true,
                        Parent = Scrolling;
                    });

                    Library:AddToRegistry(Button, {
                        BackgroundColor3 = 'MainColor';
                        BorderColor3 = 'OutlineColor';
                    });

                    local ButtonLabel = Library:CreateLabel({
                        Active = false;
                        Size = UDim2.new(1, -6, 1, 0);
                        Position = UDim2.new(0, 6, 0, 0);
                        TextSize = 14;
                        Text = Value;
                        TextXAlignment = Enum.TextXAlignment.Left;
                        ZIndex = 25;
                        Parent = Button;
                    });

                    Library:OnHighlight(Button, Button,
                        { BorderColor3 = 'AccentColor', ZIndex = 24 },
                        { BorderColor3 = 'OutlineColor', ZIndex = 23 }
                    );

                    local Selected;

                    if Info.Multi then
                        Selected = Dropdown.Value[Value];
                    else
                        Selected = Dropdown.Value == Value;
                    end;

                    function Table:UpdateButton()
                        if Info.Multi then
                            Selected = Dropdown.Value[Value];
                        else
                            Selected = Dropdown.Value == Value;
                        end;

                        ButtonLabel.TextColor3 = Selected and Library.AccentColor or Library.FontColor;
                        Library.RegistryMap[ButtonLabel].Properties.TextColor3 = Selected and 'AccentColor' or 'FontColor';
                    end;

                    ButtonLabel.InputBegan:Connect(function(Input)
                        if Input.UserInputType == Enum.UserInputType.MouseButton1 then
                            local Try = not Selected;

                            if Dropdown:GetActiveValues() == 1 and (not Try) and (not Info.AllowNull) then
                            else
                                if Info.Multi then
                                    Selected = Try;

                                    if Selected then
                                        Dropdown.Value[Value] = true;
                                    else
                                        Dropdown.Value[Value] = nil;
                                    end;
                                else
                                    Selected = Try;

                                    if Selected then
                                        Dropdown.Value = Value;
                                    else
                                        Dropdown.Value = nil;
                                    end;

                                    for _, OtherButton in next, Buttons do
                                        OtherButton:UpdateButton();
                                    end;
                                end;

                                Table:UpdateButton();
                                Dropdown:Display();

                                Library:SafeCallback(Dropdown.Callback, Dropdown.Value);
                                Library:SafeCallback(Dropdown.Changed, Dropdown.Value);

                                Library:AttemptSave();
                            end;
                        end;
                    end);

                    Table:UpdateButton();
                    Buttons[Value] = Table;
                    
                    -- Update canvas size dynamically
                    Scrolling.CanvasSize = UDim2.fromOffset(0, renderedCount * 20 + 1)
                    
                    -- Small delay between items to prevent lag
                    if loaded % 5 == 0 then
                        task.wait()
                    end
                end
                
                loadingMore = false
            end

            -- Initial load
            if lazyLoadEnabled then
                -- Load initial batch immediately
                loadMoreItems(INITIAL_LOAD_COUNT)
                
                -- Set up scroll detection for lazy loading
                local lastLoadTime = 0
                local scrollConnection
                scrollConnection = Scrolling:GetPropertyChangedSignal('CanvasPosition'):Connect(function()
                    if not ListOuter.Visible then
                        if scrollConnection then scrollConnection:Disconnect() end
                        return
                    end
                    
                    if renderedCount >= #allValues then
                        if scrollConnection then scrollConnection:Disconnect() end
                        return
                    end
                    
                    local scrollPos = Scrolling.CanvasPosition.Y
                    local scrollHeight = Scrolling.CanvasSize.Y.Offset
                    local viewHeight = Scrolling.AbsoluteSize.Y
                    
                    -- Load more when scrolling near bottom (loads 5 items at a time)
                    if scrollPos + viewHeight >= scrollHeight - 30 then
                        local now = tick()
                        if now - lastLoadTime > 0.05 then
                            lastLoadTime = now
                            task.spawn(function()
                                loadMoreItems(5)
                            end)
                        end
                    end
                end)
                
                -- Also trigger load when dropdown opens
                local openConnection
                openConnection = ListOuter:GetPropertyChangedSignal('Visible'):Connect(function()
                    if ListOuter.Visible and renderedCount < #allValues then
                        task.spawn(function()
                            loadMoreItems(10)
                        end)
                    end
                end)
            else
                -- Load all items immediately for small lists
                Values = allValues
            end

            if not lazyLoadEnabled then
                for Idx, Value in next, Values do
                local Table = {};

                Count = Count + 1;

                local Button = Library:Create('Frame', {
                    BackgroundColor3 = Library.MainColor;
                    BorderColor3 = Library.OutlineColor;
                    BorderMode = Enum.BorderMode.Middle;
                    Size = UDim2.new(1, -1, 0, 20);
                    ZIndex = 23;
                    Active = true,
                    Parent = Scrolling;
                });

                Library:AddToRegistry(Button, {
                    BackgroundColor3 = 'MainColor';
                    BorderColor3 = 'OutlineColor';
                });

                local ButtonLabel = Library:CreateLabel({
                    Active = false;
                    Size = UDim2.new(1, -6, 1, 0);
                    Position = UDim2.new(0, 6, 0, 0);
                    TextSize = 14;
                    Text = Value;
                    TextXAlignment = Enum.TextXAlignment.Left;
                    ZIndex = 25;
                    Parent = Button;
                });

                Library:OnHighlight(Button, Button,
                    { BorderColor3 = 'AccentColor', ZIndex = 24 },
                    { BorderColor3 = 'OutlineColor', ZIndex = 23 }
                );

                local Selected;

                if Info.Multi then
                    Selected = Dropdown.Value[Value];
                else
                    Selected = Dropdown.Value == Value;
                end;

                function Table:UpdateButton()
                    if Info.Multi then
                        Selected = Dropdown.Value[Value];
                    else
                        Selected = Dropdown.Value == Value;
                    end;

                    ButtonLabel.TextColor3 = Selected and Library.AccentColor or Library.FontColor;
                    Library.RegistryMap[ButtonLabel].Properties.TextColor3 = Selected and 'AccentColor' or 'FontColor';
                end;

                ButtonLabel.InputBegan:Connect(function(Input)
                    if Input.UserInputType == Enum.UserInputType.MouseButton1 then
                        local Try = not Selected;

                        if Dropdown:GetActiveValues() == 1 and (not Try) and (not Info.AllowNull) then
                        else
                            if Info.Multi then
                                Selected = Try;

                                if Selected then
                                    Dropdown.Value[Value] = true;
                                else
                                    Dropdown.Value[Value] = nil;
                                end;
                            else
                                Selected = Try;

                                if Selected then
                                    Dropdown.Value = Value;
                                else
                                    Dropdown.Value = nil;
                                end;

                                for _, OtherButton in next, Buttons do
                                    OtherButton:UpdateButton();
                                end;
                            end;

                            Table:UpdateButton();
                            Dropdown:Display();

                            Library:SafeCallback(Dropdown.Callback, Dropdown.Value);
                            Library:SafeCallback(Dropdown.Changed, Dropdown.Value);

                            Library:AttemptSave();
                        end;
                    end;
                end);

                Table:UpdateButton();
                Dropdown:Display();

                Buttons[Button] = Table;
            end;
            end -- Close if not lazyLoadEnabled

            Scrolling.CanvasSize = UDim2.fromOffset(0, (Count * 20) + 1);

            local Y = math.clamp(Count * 20, 0, MAX_DROPDOWN_ITEMS * 20) + 1;
            RecalculateListSize(Y);
        end;

